export async function executeInitiative(
  id: string
): Promise<{ jobId?: string; error?: string }> {
  const initiative = await db.initiativeAction.findUnique({ where: { id } });
  if (!initiative) {
    return { error: "Initiative not found" };
  }
  if (initiative.status !== "accepted" && initiative.status !== "proposed") {
    return { error: `Initiative cannot be executed from status='${initiative.status}'` };
  }
  if (initiative.expiresAt && initiative.expiresAt.getTime() < Date.now()) {
    await db.initiativeAction.update({
      where: { id },
      data: { status: "expired" },
    });
    return { error: "Initiative has expired" };
  }

  // Non-dispatchable kinds just get marked executed.
  if (initiative.kind !== "autonomous_workflow" && initiative.kind !== "research_task") {
    await db.initiativeAction.update({
      where: { id },
      data: { status: "executed", executedAt: new Date() },
    });
    await emit("info", "companion", `Initiative executed (non-dispatchable): ${initiative.title}`, {
      initiativeId: id,
      kind: initiative.kind,
    });
    return {};
  }

  // Dispatchable kinds: create a root Task and enqueue a durable job.
  try {
    const suggestion: InitiativeSuggestion = {
      id: initiative.id,
      kind: initiative.kind as InitiativeSuggestion["kind"],
      title: initiative.title,
      description: initiative.description,
      rationale: initiative.rationale,
      priority: initiative.priority,
      sourceSignals: readJSON<Record<string, unknown>>(initiative.sourceSignals, {}),
      linkedProjectId: initiative.linkedProjectId ?? undefined,
      linkedGoalId: initiative.linkedGoalId ?? undefined,
    };

    const taskKind: TaskKind =
      initiative.kind === "research_task" ? "retrieve" : "reason";

    const rootTask = await db.task.create({
      data: {
        workspaceId: DEFAULT_WORKSPACE_ID,
        title: initiative.title,
        input: writeJSON({
          initiativeId: initiative.id,
          description: initiative.description,
          rationale: initiative.rationale,
          agentContext: companionAgentContext(suggestion),
        }),
        output: writeJSON({}),
        status: "pending",
        depth: 0,
        kind: taskKind,
      },
    });

    const agentContext = companionAgentContext(suggestion);
    const plan = {
      kinds: [taskKind] as TaskKind[],
      reasoning: `Companion-initiated ${initiative.kind}: ${initiative.title}`,
    };

    const job = await enqueue({
      taskId: rootTask.id,
      title: initiative.title,
      workspaceId: DEFAULT_WORKSPACE_ID,
      agentContext,
      plan,
    });

    await db.initiativeAction.update({
      where: { id },
      data: { status: "executed", executedAt: new Date() },
    });

    await emit("info", "companion", `Initiative dispatched: ${initiative.title}`, {
      initiativeId: id,
      jobId: job.id,
      taskId: rootTask.id,
    });

    return { jobId: job.id };
  } catch (e) {
    const msg = (e as Error)?.message ?? String(e);
    await emit("error", "companion", `Initiative execution failed: ${initiative.title}`, {
      initiativeId: id,
      error: msg.slice(0, 300),
    });
    return { error: msg };
  }
}

// ─── 6. Companion State Aggregator ──────────────────────────────────────────

export async function getCompanionState(opts: {
  profileId?: string;
}): Promise<CompanionState> {
  const profileId = opts.profileId ?? DEFAULT_PROFILE_ID;

  // UserProfile.profileId is the unique key (matches input); RelationshipState
  // FKs to UserProfile.id, so resolve it (find-only here; this is a read).
  const userProfileId = await findUserProfileId(profileId);

  const [
    userProfile,
    relProfile,
    relState,
    traits,
    activeProjects,
    openBlockers,
    initiativeCounts,
    recentDecisions,
    proposedInitiatives,
  ] = await Promise.all([
    db.userProfile.findUnique({ where: { profileId } }),
    db.relationshipProfile.findUnique({ where: { id: profileId } }),
    userProfileId
      ? db.relationshipState.findUnique({ where: { profileId: userProfileId } })
      : Promise.resolve(null),
    getActiveTraits(),
    db.project.findMany({
      where: { profileId, status: "active" },
      select: { id: true, progress: true },
    }),
    db.projectBlocker.count({ where: { status: "open" } }),
    db.initiativeAction.groupBy({
      by: ["status"],
      _count: true,
    }),
    db.projectDecision.findMany({
      orderBy: { decidedAt: "desc" },
      take: 5,
    }),
    db.initiativeAction.findMany({
      where: { status: "proposed", expiresAt: { gte: new Date() } },
      orderBy: { priority: "desc" },
      take: 5,
    }),
  ]);

  const name =
    userProfile?.preferredName ||
    relProfile?.name ||
    "User";
  const expertiseLevel = userProfile?.expertiseLevel ?? "generalist";

  const trustLevel = relState?.trustLevel ?? 0.5;
  const rapportLevel = relState?.rapportLevel ?? 0.5;
  const engagementTrend = relState?.engagementTrend ?? "stable";
  const totalInteractions = relState?.totalInteractions ?? 0;
  const openThreads = readJSON<string[]>(relState?.openThreads ?? "[]", []);

  const totalProgress = activeProjects.length > 0
    ? Math.round(
        activeProjects.reduce((s, p) => s + p.progress, 0) / activeProjects.length
      )
    : 0;

  const initiativeByStatus = (status: string): number =>
    initiativeCounts.find((c) => c.status === status)?._count ?? 0;

  const suggestedActions: InitiativeSuggestion[] = proposedInitiatives.map((i) => ({
    id: i.id,
    kind: normalizeInitiativeKind(i.kind),
    title: i.title,
    description: i.description,
    rationale: i.rationale,
    priority: i.priority,
    sourceSignals: readJSON<Record<string, unknown>>(i.sourceSignals, {}),
    linkedProjectId: i.linkedProjectId ?? undefined,
    linkedGoalId: i.linkedGoalId ?? undefined,
  }));

  return {
    profile: { name, preferredName: name, expertiseLevel },
    traits,
    relationship: {
      trustLevel,
      rapportLevel,
      engagementTrend,
      totalInteractions,
      openThreadsCount: openThreads.length,
    },
    projects: {
      active: activeProjects.length,
      totalProgress,
      openBlockers,
    },
    initiatives: {
      proposed: initiativeByStatus("proposed"),
      accepted: initiativeByStatus("accepted"),
      executed: initiativeByStatus("executed"),
    },
    recentDecisions,
    suggestedActions,
  };
}
