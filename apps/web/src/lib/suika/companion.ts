export async function detectProjectFromMessage(
  message: string,
  profileId?: string
): Promise<Project | null> {
  const lower = message.toLowerCase();
  const projects = await db.project.findMany({
    where: { profileId: profileId ?? DEFAULT_PROFILE_ID },
    take: 200,
  });
  // Prefer the longest-name match (most specific), tiebreak by priority desc.
  const matches = projects.filter((p) => p.name && lower.includes(p.name.toLowerCase()));
  if (matches.length === 0) return null;
  matches.sort((a, b) => {
    if (b.name.length !== a.name.length) return b.name.length - a.name.length;
    return b.priority - a.priority;
  });
  return matches[0];
}

// ─── 3. Conversation Analysis + Fact Extraction ────────────────────────────

const CONVERSATION_ANALYSIS_SYSTEM_PROMPT = `You are SUIKA's conversation analyzer. Read the conversation and produce a STRICT JSON object describing it. Output ONLY the JSON — no markdown fences, no commentary.

JSON schema (fill every field, use empty arrays when nothing applies):
{
  "summary": "1-2 sentence summary of the conversation",
  "topics": ["topic1", "topic2"],
  "decisions": ["decision that was made", "..."],
  "actionItems": ["concrete next action", "..."],
  "facts": [
    {"kind": "personal|professional|technical|preference|goal", "content": "fact about the user", "confidence": 0.0-1.0}
  ],
  "sentiment": "positive|neutral|negative|mixed",
  "projectMentions": ["project name as spoken by user", "..."],
  "nextActions": ["follow-up SUIKA could take", "..."]
}

Rules:
- Extract facts ONLY about the user (not about SUIKA).
- Confidence is a number 0..1.
- sentiment is one of the 4 enum values.
- Project mentions are names of projects the user referenced (verbatim if possible).`;

export async function analyzeConversation(opts: {
  messages: Array<{ role: "user" | "assistant"; content: string }>;
  profileId?: string;
}): Promise<ConversationAnalysis> {
  const profileId = opts.profileId ?? DEFAULT_PROFILE_ID;
  const messages = opts.messages ?? [];

  if (messages.length === 0) {
    return emptyAnalysis();
  }

  const transcript = messages
    .map((m) => `${m.role.toUpperCase()}: ${m.content}`)
    .join("\n\n");

  const raw = await callLLM(
    CONVERSATION_ANALYSIS_SYSTEM_PROMPT,
    `Analyze this conversation:\n\n${transcript.slice(0, 8000)}`
  );

  const parsed = parseLLMJson<Partial<ConversationAnalysis>>(raw);
  if (parsed && typeof parsed === "object" && parsed.summary) {
    return normalizeAnalysis(parsed);
  }

  // ── Heuristic fallback ────────────────────────────────────────────────────
  return heuristicAnalysis(messages, profileId);
}

function emptyAnalysis(): ConversationAnalysis {
  return {
    summary: "",
    topics: [],
    decisions: [],
    actionItems: [],
    facts: [],
    sentiment: "neutral",
    projectMentions: [],
    nextActions: [],
  };
}

function normalizeAnalysis(p: Partial<ConversationAnalysis>): ConversationAnalysis {
  const sentiment = (p.sentiment as ConversationAnalysis["sentiment"]) ?? "neutral";
  return {
    summary: typeof p.summary === "string" ? p.summary.slice(0, 1000) : "",
    topics: Array.isArray(p.topics) ? p.topics.filter((t) => typeof t === "string").slice(0, 20) : [],
    decisions: Array.isArray(p.decisions) ? p.decisions.filter((t) => typeof t === "string").slice(0, 20) : [],
    actionItems: Array.isArray(p.actionItems) ? p.actionItems.filter((t) => typeof t === "string").slice(0, 20) : [],
    facts: Array.isArray(p.facts)
      ? p.facts
          .filter((f) => f && typeof f.content === "string")
          .slice(0, 20)
          .map((f) => ({
            kind: typeof f.kind === "string" ? f.kind : "personal",
            content: String(f.content).slice(0, 500),
            confidence: typeof f.confidence === "number" ? clamp(f.confidence, 0, 1) : 0.6,
          }))
      : [],
    sentiment: ["positive", "neutral", "negative", "mixed"].includes(sentiment)
      ? sentiment
      : "neutral",
    projectMentions: Array.isArray(p.projectMentions)
      ? p.projectMentions.filter((t) => typeof t === "string").slice(0, 10)
      : [],
    nextActions: Array.isArray(p.nextActions) ? p.nextActions.filter((t) => typeof t === "string").slice(0, 20) : [],
  };
}

function heuristicAnalysis(
  messages: Array<{ role: "user" | "assistant"; content: string }>,
  _profileId: string
): ConversationAnalysis {
  const userMsgs = messages.filter((m) => m.role === "user").map((m) => m.content);
  const lastUser = userMsgs[userMsgs.length - 1] ?? "";
  const allText = messages.map((m) => m.content).join(" ");
  const tokens = tokenize(allText);
  return {
    summary: lastUser.slice(0, 200) || "(no user message)",
    topics: tokens.slice(0, 5),
    decisions: [],
    actionItems: [],
    facts: [],
    sentiment: "neutral",
    projectMentions: [],
    nextActions: [],
  };
}

export async function persistConversationAnalysis(opts: {
  analysis: ConversationAnalysis;
  profileId?: string;
  channel?: "text" | "voice";
  messageCount: number;
  startedAt: Date;
  endedAt?: Date;
}): Promise<{ summaryId: string; factIds: string[] }> {
  const profileId = opts.profileId ?? DEFAULT_PROFILE_ID;
  const channel = opts.channel ?? "text";
  const analysis = opts.analysis;

  // Phase 4.3 tables (ConversationSummary, UserFact, RelationshipState) FK to
  // UserProfile.id (cuid), not to the input profileId string. Resolve it once.
  const userProfileId = await resolveUserProfileId(profileId);

  // 1. Create ConversationSummary row
  const summary = await db.conversationSummary.create({
    data: {
      profileId: userProfileId,
      channel,
      summary: analysis.summary,
      topics: writeJSON(analysis.topics),
      decisions: writeJSON(analysis.decisions),
      actionItems: writeJSON(analysis.actionItems),
      sentiment: analysis.sentiment,
      startedAt: opts.startedAt,
      endedAt: opts.endedAt ?? new Date(),
      messageCount: opts.messageCount,
    },
  });

  // 2. Create UserFact rows (dedup by content for this profile)
  const factIds: string[] = [];
  for (const fact of analysis.facts) {
    const existing = await db.userFact.findFirst({
      where: { profileId: userProfileId, content: fact.content },
      select: { id: true },
    });
    if (existing) {
      factIds.push(existing.id);
      continue;
    }
    const created = await db.userFact.create({
      data: {
        profileId: userProfileId,
        kind: fact.kind,
        content: fact.content,
        confidence: fact.confidence,
        source: "conversation",
      },
    });
    factIds.push(created.id);
  }

  // 3. Update RelationshipState: bump counters, update timestamp, append openThreads
  const existingState = await db.relationshipState.findUnique({
    where: { profileId: userProfileId },
  });
  const currentThreads = readJSON<string[]>(
    existingState?.openThreads ?? "[]",
    []
  );
  // Append new nextActions as open threads (dedupe, cap at 20)
  const mergedThreads = Array.from(
    new Set([...currentThreads, ...analysis.nextActions])
  ).slice(0, 20);
  const isPositive = analysis.sentiment === "positive";
  const now = new Date();
  if (existingState) {
    await db.relationshipState.update({
      where: { profileId: userProfileId },
      data: {
        totalInteractions: { increment: 1 },
        positiveInteractions: { increment: isPositive ? 1 : 0 },
        lastInteractionAt: now,
        openThreads: writeJSON(mergedThreads),
      },
    });
  } else {
    await db.relationshipState.create({
      data: {
        profileId: userProfileId,
        totalInteractions: 1,
        positiveInteractions: isPositive ? 1 : 0,
        lastInteractionAt: now,
        openThreads: writeJSON(mergedThreads),
      },
    });
  }

  // 4. Project-mention progress heuristic
  if (analysis.projectMentions.length > 0 && analysis.actionItems.length > 0) {
    const completionSignals = analysis.actionItems.filter((a) =>
      /\b(complete|done|finish|ship|deliver|merged?|resolved?)\b/i.test(a)
    );
    if (completionSignals.length > 0) {
      const lowerMentions = analysis.projectMentions.map((m) => m.toLowerCase());
      const projects = await db.project.findMany({
        where: { profileId, status: "active" },
        select: { id: true, name: true, progress: true },
      });
      for (const p of projects) {
        if (lowerMentions.some((m) => p.name.toLowerCase().includes(m) || m.includes(p.name.toLowerCase()))) {
          const newProgress = clamp(p.progress + 5, 0, 100);
          await updateProjectProgress(p.id, newProgress);
        }
      }
    }
  }

  // 5. Emit event
  await emit("info", "companion", "Conversation summarized", {
    summaryId: summary.id,
    topics: analysis.topics,
    factCount: factIds.length,
    sentiment: analysis.sentiment,
  });

  return { summaryId: summary.id, factIds };
}

// ─── 4. Personality Engine ──────────────────────────────────────────────────

const DEFAULT_TRAITS: Omit<CompanionTraitsDTO, "version" | "rationale"> = {
  curiosity: 0.7,
  confidence: 0.6,
  focus: 0.7,
  creativity: 0.65,
  empathy: 0.75,
  persistence: 0.7,
};

function toTraitsDTO(t: CompanionTraits): CompanionTraitsDTO {
  return {
    curiosity: t.curiosity,
    confidence: t.confidence,
    focus: t.focus,
    creativity: t.creativity,
    empathy: t.empathy,
    persistence: t.persistence,
    version: t.version,
    rationale: t.rationale,
  };
}

export async function getActiveTraits(): Promise<CompanionTraitsDTO> {
  const active = await db.companionTraits.findFirst({
    where: { isActive: true },
    orderBy: { version: "desc" },
  });
  if (active) return toTraitsDTO(active);

  // Seed initial trait vector on first boot.
  const seeded = await db.companionTraits.create({
    data: {
      version: 1,
      isActive: true,
      curiosity: DEFAULT_TRAITS.curiosity,
      confidence: DEFAULT_TRAITS.confidence,
      focus: DEFAULT_TRAITS.focus,
      creativity: DEFAULT_TRAITS.creativity,
      empathy: DEFAULT_TRAITS.empathy,
      persistence: DEFAULT_TRAITS.persistence,
      rationale: "Initial trait vector seeded on first access.",
    },
  });
  await emit("info", "companion", "Companion traits seeded", {
    version: 1,
  });
  return toTraitsDTO(seeded);
}

export async function evolveTraits(opts: {
  success?: boolean;
  failure?: boolean;
  reflection?: string;
  userSentiment?: "positive" | "neutral" | "negative";
}): Promise<CompanionTraitsDTO> {
  const current = await getActiveTraits();
  const currentRow = await db.companionTraits.findFirst({
    where: { isActive: true },
    orderBy: { version: "desc" },
  });
  if (!currentRow) {
    // Should not happen after getActiveTraits(), but guard anyway.
    return current;
  }

  const deltas: Record<string, number> = {
    curiosity: 0,
    confidence: 0,
    focus: 0,
    creativity: 0,
    empathy: 0,
    persistence: 0,
  };

  if (opts.success) {
    deltas.confidence += 0.02;
    deltas.persistence += 0.01;
    if (opts.userSentiment === "positive") {
      deltas.empathy += 0.005;
    }
  }
  if (opts.failure) {
    deltas.confidence -= 0.03;
    deltas.curiosity += 0.01;
    deltas.persistence += 0.005;
  }
  if (opts.reflection && opts.reflection.trim().length > 0) {
    deltas.creativity += 0.01;
    deltas.curiosity += 0.005;
  }
  if (opts.userSentiment === "negative") {
    deltas.empathy += 0.01;
  }

  const next = {
    curiosity: clamp01(round(current.curiosity + deltas.curiosity)),
    confidence: clamp01(round(current.confidence + deltas.confidence)),
    focus: clamp01(round(current.focus + deltas.focus)),
    creativity: clamp01(round(current.creativity + deltas.creativity)),
    empathy: clamp01(round(current.empathy + deltas.empathy)),
    persistence: clamp01(round(current.persistence + deltas.persistence)),
  };

  // Clamp confidence floor at 0.2 on failure (per spec).
  if (opts.failure && next.confidence < 0.2) {
    next.confidence = 0.2;
  }

  const rationaleParts: string[] = [];
  if (opts.success) rationaleParts.push("success");
  if (opts.failure) rationaleParts.push("failure");
  if (opts.reflection) rationaleParts.push("reflection");
  if (opts.userSentiment) rationaleParts.push(`userSentiment=${opts.userSentiment}`);
  const rationale = rationaleParts.length
    ? `Evolved from v${current.version} (${rationaleParts.join(", ")}).`
    : `Evolved from v${current.version}.`;

  // Deactivate old, create new versioned row (immutable history).
  await db.$transaction([
    db.companionTraits.update({
      where: { id: currentRow.id },
      data: { isActive: false },
    }),
    db.companionTraits.create({
      data: {
        version: current.version + 1,
        isActive: true,
        curiosity: next.curiosity,
        confidence: next.confidence,
        focus: next.focus,
        creativity: next.creativity,
        empathy: next.empathy,
        persistence: next.persistence,
        rationale,
      },
    }),
  ]);

  await emit("info", "companion", "Traits evolved", {
    from: current.version,
    to: current.version + 1,
    deltas,
  });

  return {
    ...next,
    version: current.version + 1,
    rationale,
  };
}

// ─── 5. Initiative Engine ──────────────────────────────────────────────────

const INITIATIVE_SYSTEM_PROMPT = `You are SUIKA's initiative engine. Given a set of signals (active goals, open blockers, pending/dead-lettered jobs, recently failed tasks, open threads), propose the MOST USEFUL actions SUIKA could take right now for the user. Output ONLY a STRICT JSON array — no markdown fences, no commentary.

Array element schema:
{
  "kind": "suggestion|autonomous_workflow|research_task|reminder",
  "title": "concise action title (max 80 chars)",
  "description": "what SUIKA would do (1-2 sentences)",
  "rationale": "why this is useful now, given the signals",
  "priority": 0-100,
  "linkedProjectId": null,
  "linkedGoalId": null
}

Rules:
- kind is one of the 4 enum values.
- "suggestion" = passive advice; "autonomous_workflow" = SUIKA runs a workflow; "research_task" = SUIKA gathers info; "reminder" = surface something the user may have forgotten.
- priority is 0..100 (higher = more urgent).
- linkedProjectId / linkedGoalId are null unless the signal explicitly references an id.
- Be concrete and specific. Each suggestion must be actionable within 24 hours.
- Limit to the top N suggestions by impact.`;

interface InitiativeSignals {
  goals: Array<{ id: string; title: string; status: string; progress: number }>;
  blockers: Array<{ id: string; title: string; projectId: string; severity: string }>;
  jobs: Array<{ id: string; title: string; status: string }>;
  failedTasks: Array<{ id: string; title: string }>;
  openThreads: string[];
}

export async function generateInitiatives(opts: {
  profileId?: string;
  maxSuggestions?: number;
}): Promise<InitiativeSuggestion[]> {
  const profileId = opts.profileId ?? DEFAULT_PROFILE_ID;
  const maxSuggestions = Math.max(1, Math.min(opts.maxSuggestions ?? 5, 20));

  // RelationshipState FKs to UserProfile.id — resolve once, may be null on a
  // fresh boot (no UserProfile row yet → no open threads, which is fine).
  const userProfileId = await findUserProfileId(profileId);

  // ── Gather signals ───────────────────────────────────────────────────────
  const [goals, blockers, jobs, failedTasks, relState] = await Promise.all([
    db.relationshipGoal.findMany({
      where: { profileId, status: "active" },
      select: { id: true, title: true, status: true, progress: true },
      take: 20,
    }),
    db.projectBlocker.findMany({
      where: { status: "open" },
      select: { id: true, title: true, projectId: true, severity: true },
      take: 20,
    }),
    db.executionJob.findMany({
      where: { status: { in: ["pending", "dead_lettered"] } },
      select: { id: true, title: true, status: true },
      take: 20,
    }),
    db.task.findMany({
      where: { status: "failed" },
      select: { id: true, title: true },
      orderBy: { createdAt: "desc" },
      take: 5,
    }),
    userProfileId
      ? db.relationshipState.findUnique({
          where: { profileId: userProfileId },
          select: { openThreads: true },
        })
      : Promise.resolve(null),
  ]);

  const signals: InitiativeSignals = {
    goals,
    blockers,
    jobs,
    failedTasks,
    openThreads: readJSON<string[]>(relState?.openThreads ?? "[]", []),
  };

  const totalSignals =
    signals.goals.length +
    signals.blockers.length +
    signals.jobs.length +
    signals.failedTasks.length +
    signals.openThreads.length;

  if (totalSignals === 0) {
    return [];
  }

  // ── LLM call ──────────────────────────────────────────────────────────────
  const prompt = `Signals:\n${JSON.stringify(signals, null, 2)}\n\nPropose up to ${maxSuggestions} initiative suggestions. Return ONLY a JSON array.`;
  const raw = await callLLM(INITIATIVE_SYSTEM_PROMPT, prompt);

  let suggestions: InitiativeSuggestion[] = [];
  const parsed = parseLLMJson<InitiativeSuggestion[] | { suggestions: InitiativeSuggestion[] }>(raw);
  if (Array.isArray(parsed)) {
    suggestions = parsed;
  } else if (parsed && Array.isArray((parsed as { suggestions?: InitiativeSuggestion[] }).suggestions)) {
    suggestions = (parsed as { suggestions: InitiativeSuggestion[] }).suggestions;
  }

  // ── Heuristic fallback ────────────────────────────────────────────────────
  if (suggestions.length === 0) {
    suggestions = heuristicInitiatives(signals, maxSuggestions);
  }

  // Normalize + bound
  suggestions = suggestions
    .filter((s) => s && typeof s.title === "string" && s.title.trim().length > 0)
    .slice(0, maxSuggestions)
    .map((s) => ({
      kind: normalizeInitiativeKind(s.kind),
      title: String(s.title).slice(0, 200),
      description: typeof s.description === "string" ? s.description.slice(0, 1000) : "",
      rationale: typeof s.rationale === "string" ? s.rationale.slice(0, 1000) : "",
      priority: clamp(Math.round(s.priority ?? 50), 0, 100),
      sourceSignals: {
        goalsCount: signals.goals.length,
        blockersCount: signals.blockers.length,
        jobsCount: signals.jobs.length,
        failedTasksCount: signals.failedTasks.length,
        openThreadsCount: signals.openThreads.length,
      },
      linkedProjectId: typeof s.linkedProjectId === "string" ? s.linkedProjectId : undefined,
      linkedGoalId: typeof s.linkedGoalId === "string" ? s.linkedGoalId : undefined,
    }));

  // ── Persist as InitiativeAction rows ──────────────────────────────────────
  const expiresAt = new Date(Date.now() + 24 * 60 * 60 * 1000);
  const persisted: InitiativeSuggestion[] = [];
  for (const s of suggestions) {
    const row = await db.initiativeAction.create({
      data: {
        kind: s.kind,
        title: s.title,
        description: s.description,
        rationale: s.rationale,
        priority: s.priority,
        status: "proposed",
        sourceSignals: writeJSON(s.sourceSignals),
        linkedProjectId: s.linkedProjectId ?? null,
        linkedGoalId: s.linkedGoalId ?? null,
        proposedAt: new Date(),
        expiresAt,
      },
    });
    persisted.push({ ...s, id: row.id });
  }

  await emit("info", "companion", `Initiative engine generated ${persisted.length} suggestions`, {
    count: persisted.length,
    signals: totalSignals,
  });

  return persisted;
}

function normalizeInitiativeKind(
  kind: string | undefined
): InitiativeSuggestion["kind"] {
  switch (kind) {
    case "suggestion":
    case "autonomous_workflow":
    case "research_task":
    case "reminder":
      return kind;
    default:
      return "suggestion";
  }
}

function heuristicInitiatives(
  signals: InitiativeSignals,
  max: number
): InitiativeSuggestion[] {
  const out: InitiativeSuggestion[] = [];
  const baseSignals = {
    goalsCount: signals.goals.length,
    blockersCount: signals.blockers.length,
    jobsCount: signals.jobs.length,
    failedTasksCount: signals.failedTasks.length,
    openThreadsCount: signals.openThreads.length,
  };

  if (signals.blockers.length > 0) {
    const b = signals.blockers[0];
    out.push({
      kind: "suggestion",
      title: `Draft mitigation for blocker: ${b.title}`,
      description: `Propose concrete steps to resolve the ${b.severity}-severity blocker on the active project.`,
      rationale: `${signals.blockers.length} open blocker(s) are blocking project progress.`,
      priority: 80,
      sourceSignals: baseSignals,
      linkedProjectId: b.projectId,
    });
  }

  for (const g of signals.goals.slice(0, 2)) {
    if (g.progress < 100) {
      out.push({
        kind: "suggestion",
        title: `Plan next milestone for: ${g.title}`,
        description: `Goal is at ${g.progress}%. Suggest the next milestone and the tasks required to reach it.`,
        rationale: `Active goal "${g.title}" is at ${g.progress}% — momentum check.`,
        priority: 60,
        sourceSignals: baseSignals,
        linkedGoalId: g.id,
      });
    }
  }

  if (signals.jobs.filter((j) => j.status === "dead_lettered").length > 0) {
    out.push({
      kind: "autonomous_workflow",
      title: "Retry dead-lettered jobs",
      description: "Re-enqueue jobs that exceeded their retry budget after a cooldown.",
      rationale: `${signals.jobs.filter((j) => j.status === "dead_lettered").length} job(s) are dead-lettered and may now succeed.`,
      priority: 50,
      sourceSignals: baseSignals,
    });
  }

  if (signals.failedTasks.length > 0) {
    out.push({
      kind: "research_task",
      title: "Diagnose recent task failures",
      description: "Investigate root cause of the most recent failed tasks and propose fixes.",
      rationale: `${signals.failedTasks.length} task(s) failed recently.`,
      priority: 55,
      sourceSignals: baseSignals,
    });
  }

  if (signals.openThreads.length > 0) {
    out.push({
      kind: "reminder",
      title: `Resume open thread: ${signals.openThreads[0].slice(0, 60)}`,
      description: "Surface the oldest unfinished discussion topic for the user to revisit.",
      rationale: `${signals.openThreads.length} open thread(s) in the relationship state.`,
      priority: 40,
      sourceSignals: baseSignals,
    });
  }

  return out.slice(0, max);
}

