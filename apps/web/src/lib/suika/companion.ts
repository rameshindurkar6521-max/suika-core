export async function updateProjectProgress(
  id: string,
  progress: number
): Promise<void> {
  const clamped = clamp(Math.round(progress), 0, 100);
  const data: { progress: number; status?: string; completedAt?: Date } = { progress: clamped };
  if (clamped >= 100) {
    data.status = "completed";
    data.completedAt = new Date();
  }
  await db.project.update({ where: { id }, data });
  await emit("debug", "project", `Project progress updated`, {
    projectId: id,
    progress: clamped,
  });
}

export async function addMilestone(input: {
  projectId: string;
  title: string;
  description?: string;
  targetDate?: Date;
  significance?: number;
}): Promise<ProjectMilestone> {
  // Append after existing milestones to preserve order.
  const existing = await db.projectMilestone.count({
    where: { projectId: input.projectId },
  });
  const milestone = await db.projectMilestone.create({
    data: {
      projectId: input.projectId,
      title: input.title,
      description: input.description ?? "",
      targetDate: input.targetDate ?? null,
      significance: clamp(Math.round(input.significance ?? 50), 0, 100),
      order: existing,
    },
  });
  await emit("info", "project", `Milestone added: ${input.title}`, {
    projectId: input.projectId,
    milestoneId: milestone.id,
  });
  return milestone;
}

