/**
 * SUIKA X — Companion Intelligence Layer.
 *
 * This module is the "always-on companion" that observes every conversation,
 * maintains a structured understanding of the user's projects and goals,
 * evolves its own personality from experience, and proactively proposes
 * initiatives. It is the bridge between the reactive Agent Runtime (which
 * executes dispatched tasks) and the proactive user-partner layer.
 *
 * Six subsystems live here:
 *
 *   1. Context Fusion Engine       — fuseContext()
 *      Composes a FusedContext from a user message by simultaneously
 *      querying projects, goals, memories, workflows, knowledge entities,
 *      user facts, recent decisions, open blockers, and open threads.
 *      Returns a natural-language digest plus structured IDs.
 *
 *   2. Project Manager             — listProjects, getProject, createProject,
 *                                     updateProjectProgress, addMilestone,
 *                                     addTask, addDecision, addBlocker,
 *                                     resolveBlocker, detectProjectFromMessage
 *      Autonomous project tracking: milestones, tasks, decisions, blockers.
 *      Each ProjectDTO carries aggregated progress metrics.
 *
 *   3. Conversation Analysis       — analyzeConversation, persistConversationAnalysis
 *      LLM-extracts summary / topics / decisions / action items / facts /
 *      sentiment / project mentions / next actions. Falls back to heuristic
 *      extraction on LLM or JSON-parse failure. Persists a ConversationSummary,
 *      dedups and persists UserFacts, updates RelationshipState, optionally
 *      bumps project progress, and emits an event.
 *
 *   4. Personality Engine          — getActiveTraits, evolveTraits
 *      Maintains a versioned CompanionTraits row. Each evolveTraits() call
 *      applies small deltas based on success/failure/reflection/sentiment,
 *      clamps to [0.1, 1.0], deactivates the old row, and creates a new
 *      versioned row (immutable history).
 *
 *   5. Initiative Engine           — generateInitiatives, listInitiatives,
 *                                     decideInitiative, executeInitiative
 *      Gathers signals (active goals, open blockers, dead-lettered jobs,
 *      failed tasks, open threads), asks the LLM for initiative suggestions,
 *      falls back to heuristics on failure, persists InitiativeAction rows
 *      with 24h expiry, and can execute accepted initiatives by enqueuing a
 *      durable job through the existing job-queue.
 *
 *   6. Companion State Aggregator  — getCompanionState
 *      Single-call snapshot of the companion's view of the user: profile,
 *      traits, relationship, projects, initiatives, recent decisions, and
 *      fresh suggested actions.
 *
 * Design notes:
 *   - All functions are async and awaitable.
 *   - LLM calls are wrapped in try/catch with graceful heuristic fallbacks.
 *   - JSON-string DB columns are parsed via readJSON() with safe defaults.
 *   - JSON-string DB columns are written via writeJSON() (never null).
 *   - IDs are cuid() auto-generated by Prisma — we never pass `id` in create data.
 *   - Prisma types (Project, ProjectMilestone, etc.) are imported from
 *     @prisma/client; DTO types are defined locally.
 */
